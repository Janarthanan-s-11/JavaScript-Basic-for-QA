// target to extract a name into individual values

let name = "batman"
let a = 0
while(a < name.length){
console.log(name[a])
a++
}

/*Line	What it does
let name = "batman"	Stores the string "batman" in name
let a = 0	Sets the loop index to 0
while (a < name.length)	Loops through each character
console.log(name[a])	Prints each character at index a
a++	Moves to the next character*/